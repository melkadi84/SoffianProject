from django.utils.translation import get_language

def _t(text, *args):
    lang = get_language() or 'en'
    lang_short = lang.split('-')[0].lower()
    translated = TRANSLATIONS.get(lang_short, {}).get(text, text)
    if args:
        try:
            return translated.format(*args)
        except Exception:
            return translated
    return translated

